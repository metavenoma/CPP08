#pragma once

#include <iostream>
#include <climits>
#include <exception>
#include <algorithm>
#include <sstream>

class	notFound: public std::exception
{
	private:
		int	_value;
	public:
		notFound(int value);
		virtual const char	*what() const throw()
		{
			std::stringstream	ss;
			static	std::string	message;
			ss << _value;
			message = "Error: " + ss.str() + " not found";
			return(message.c_str());
		}
};
